<script>
    const modal = document.getElementById("roomModal");
    const modalImg = document.getElementById("modalImage");
    const closeModal = document.getElementById("closeModal");
    const nextBtn = document.getElementById("nextVariant");
    const prevBtn = document.getElementById("prevVariant");

    const roomCards = document.querySelectorAll(".room-card");
    let currentRoomImages = [];
    let currentVariantIndex = 0;

    // Open modal with room variants
    roomCards.forEach(card => {
        card.addEventListener("click", () => {
            currentRoomImages = JSON.parse(card.dataset.images);
            currentVariantIndex = 0;
            modalImg.src = currentRoomImages[currentVariantIndex];
            modal.classList.add("active");
        });
    });

    // Close modal
    closeModal.addEventListener("click", () => modal.classList.remove("active"));

    // Next / Prev variant
    nextBtn.addEventListener("click", () => {
        if (!currentRoomImages.length) return;
        currentVariantIndex = (currentVariantIndex + 1) % currentRoomImages.length;
        modalImg.src = currentRoomImages[currentVariantIndex];
    });

    prevBtn.addEventListener("click", () => {
        if (!currentRoomImages.length) return;
        currentVariantIndex = (currentVariantIndex - 1 + currentRoomImages.length) % currentRoomImages.length;
        modalImg.src = currentRoomImages[currentVariantIndex];
    });

    // Close on outside click
    modal.addEventListener("click", e => {
        if (e.target === modal) modal.classList.remove("active");
    });

    // ESC key close
    document.addEventListener("keydown", e => {
        if (e.key === "Escape") modal.classList.remove("active");
    });
    // Attach correct image to More Info link
    document.querySelectorAll(".room-card").forEach(card => {
        let mainImage = card.dataset.main;
        let moreInfoBtn = card.querySelector(".moreInfo");

        moreInfoBtn.addEventListener("click", function (e) {
            e.stopPropagation();
            window.location.href = `/Hotel Woodz/Room-Section/MoreInfoPage.php?img=${encodeURIComponent(mainImage)}`;
        });
    });


</script>

